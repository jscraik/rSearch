#!/usr/bin/env node
/**
 * Install canonical prek-managed git hooks for this repository.
 *
 * Usage:
 *   node scripts/setup-git-hooks.js
 */

import { execFileSync } from "node:child_process";
import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const PREK_HOOK_MARKER = "# File generated by prek: https://github.com/j178/prek";
const PREK_HOOK_PATCH = [
	"# Keep prek cache/logs in repo-local .git to avoid home-dir sandbox write failures",
	'PREK_HOME="${PREK_HOME:-$HERE/../.cache/prek}"',
	'mkdir -p "$PREK_HOME" 2>/dev/null || true',
	"export PREK_HOME",
	"",
].join("\n");

function run(command, args, options = {}) {
	return execFileSync(command, args, {
		stdio: "inherit",
		...options,
	});
}

function clearLegacyLocalHooksPath() {
	let configuredPath = "";
	try {
		configuredPath = execFileSync("git", ["config", "--local", "--get", "core.hooksPath"], {
			encoding: "utf8",
			stdio: ["ignore", "pipe", "ignore"],
		}).trim();
	} catch {
		return;
	}

	if (!configuredPath) {
		return;
	}

	console.log(`Removing legacy local core.hooksPath: ${configuredPath}`);
	run("git", ["config", "--local", "--unset", "core.hooksPath"]);
}

function patchInstalledPrekHooks() {
	const hooksDir = resolve(process.cwd(), ".git/hooks");
	if (!existsSync(hooksDir)) {
		return 0;
	}

	let patchedCount = 0;
	for (const hookName of readdirSync(hooksDir)) {
		const hookPath = resolve(hooksDir, hookName);
		let hookContent = "";
		try {
			hookContent = readFileSync(hookPath, "utf8");
		} catch {
			continue;
		}

		if (!hookContent.includes(PREK_HOOK_MARKER)) {
			continue;
		}
		if (hookContent.includes('PREK_HOME="${PREK_HOME:-$HERE/../.cache/prek}"')) {
			continue;
		}

		const patched = hookContent.replace(
			'fi\n\nexec "$PREK" hook-impl',
			`fi\n\n${PREK_HOOK_PATCH}exec "$PREK" hook-impl`,
		);
		if (patched === hookContent) {
			continue;
		}

		writeFileSync(hookPath, patched);
		patchedCount += 1;
	}

	return patchedCount;
}

clearLegacyLocalHooksPath();
run("prek", ["install"]);
const patchedCount = patchInstalledPrekHooks();
if (patchedCount > 0) {
	console.log(`Patched ${patchedCount} prek hook shim(s) with repo-local PREK_HOME`);
}
console.log("Installed canonical prek hooks");
